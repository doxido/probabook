# Pearson correlation coefficient
def autocorrelation(data):
    n=len(data)
    nextData   = [data[i]*data[(i+1)%n] for i in range(n)]
    squareData = [data[i]**2 for i in range(n)]
    autocorrel = (n*sum(nextData) - sum(data)**2 ) / ( n*sum(squareData) - sum(data)**2)
    return autocorrel

